/*
Display "Hello, Kotlin!" on the console, but with "Kotlin" appearing on the
next line. You can display two separate `String`s to achieve this:

```text
Hello,
Kotlin!
```
*/

fun main() {
    println("Hello,")
    println("Kotlin!")
}

/* Output:
Hello,
Kotlin!
*/